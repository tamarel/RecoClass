    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.menu_signOut) {
            ParseUser.logOutInBackground();
            Intent intent = new Intent(CalendarActivity.this,MainActivity.class);
            startActivity(intent);
            return true;
        }
        else if ( id == R.id.menu_aboutUs){
            Intent intent = new Intent(CalendarActivity.this,AboutUsActivity.class);
            startActivity(intent);
            return true;
        }
        else if ( id == R.id.menu_calendar){

            return true;
        }
        else if ( id == R.id.menu_addCourse){
            Intent intent = new Intent(CalendarActivity.this,PersonGroupActivity.class);
            startActivity(intent);
            return true;
        }
        else if ( id == R.id.menu_goMenu){
            Intent intent = new Intent(CalendarActivity.this,MenuActivity.class);
            startActivity(intent);

            return true;
        }
        else if ( id == R.id.menu_settings){
            Intent intent = new Intent(CalendarActivity.this,SettingsActivity.class);
            startActivity(intent);
            return true;
        }

        return super.onOptionsItemSelected(item);
    }
